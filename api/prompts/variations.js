// Вариации для разных попыток генерации

export const getVariationPrompt = (attempt, productName) => {
  const variations = [
    // Попытка 1 (оригинал)
    () => `
### **СТИЛЬ: КЛАССИЧЕСКИЙ ПРЕМИУМ**
Сделай эту попытку №1 с акцентом на классическую премиальную эстетику:
- Товар расположен по центру, строгая композиция
- Освещение: мягкое, студийное, с одним основным источником
- Фон: нейтральный градиент (светлый → тёмный)
- Текст: минималистично, только название и цена
- Цветовая гамма: сдержанная, под категорию товара
`,

    // Попытка 2 — динамичная
    () => `
### **СТИЛЬ: ДИНАМИЧНЫЙ И СОВРЕМЕННЫЙ**
Сделай эту попытку №2 максимально динамичной:
- Товар в движении (эффект разлёта частиц, лёгкое вращение)
- Освещение: контрастное, с цветными акцентами
- Фон: абстрактные геометрические фигуры
- Текст: крупные плашки с характеристиками, парящие в воздухе
- Добавь микро-взрывы цвета вокруг ключевых особенностей
`,

    // Попытка 3 — минимализм
    () => `
### **СТИЛЬ: МИНИМАЛИЗМ И ВЫСОКИЕ ТЕХНОЛОГИИ**
Сделай эту попытку №3 в стиле Apple:
- Товар на абсолютно белом или чёрном фоне
- Освещение: идеально ровное, без теней
- Текст: тонкий шрифт, только самое важное
- Композиция: товар смещён в сторону, текст — строгими колонками
- Цветовая гамма: монохром с одним акцентным цветом
`,

    // Попытка 4 — агрессивный маркетинг
    () => `
### **СТИЛЬ: АГРЕССИВНЫЙ МАРКЕТИНГ**
Сделай эту попытку №4 максимально продающей:
- Крупные плашки со скидками (но без цифр, просто "ВЫГОДА")
- Эффект "стекла" — товар как за витриной
- Освещение: неоновые блики, лазерные лучи на фоне
- Текст: жирный, с градиентами, бросающийся в глаза
- Добавь элементы "хит", "бестселлер" (визуально, но без слов)
`,

    // Попытка 5 — креатив
    () => `
### **СТИЛЬ: КРЕАТИВНЫЙ И НЕОБЫЧНЫЙ**
Сделай эту попытку №5 самой креативной:
- Нестандартный ракурс (снизу, сверху, через лупу)
- Фон: фантазийный, как из научной фантастики
- Товар "разобран" на составляющие (если возможно)
- Текст: леттеринг, рукописный шрифт для акцентов
- Цветовая гамма: неожиданные сочетания (розовый с зелёным)
`
  ];

  // Если попытка выходит за пределы (например, 0), используем первую
  const index = Math.min(attempt, variations.length - 1);
  return variations[index]();
};

