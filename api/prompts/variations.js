// variations.js
export const getVariationPrompt = (attempt, productName) => {
  
  // Для первой попытки (attempt === 0) — ничего не добавляем
  if (attempt === 0) {
    return '';
  }
  
  // Для повторных попыток (1-4) — добавляем небольшие изменения
  const variations = [
    // Попытка 2 (attempt === 1)
    `
### **ДОПОЛНИТЕЛЬНЫЕ ИНСТРУКЦИИ (попытка 2):**
- Сделай эту версию с другим расположением товара (смести влево)
- Используй более контрастное освещение
- Добавь лёгкий эффект свечения
`,

    // Попытка 3 (attempt === 2)
    `
### **ДОПОЛНИТЕЛЬНЫЕ ИНСТРУКЦИИ (попытка 3):**
- Товар смести вправо
- Используй другую цветовую гамму (более холодную/тёплую в зависимости от товара)
- Добавь микро-блики на ключевых деталях
`,

    // Попытка 4 (attempt === 3)
    `
### **ДОПОЛНИТЕЛЬНЫЕ ИНСТРУКЦИИ (попытка 4):**
- Товар в центре, но характеристики расположи по кругу
- Используй динамичные линии
- Добавь эффект глубины
`,

    // Попытка 5 (attempt === 4)
    `
### **ДОПОЛНИТЕЛЬНЫЕ ИНСТРУКЦИИ (попытка 5):**
- Самая креативная версия
- Товар как будто парит, характеристики парят вокруг
- Используй нестандартные ракурсы
`
  ];

  // Для attempt 1-4 возвращаем соответствующую вариацию
  // attempt 1 → variations[0], attempt 2 → variations[1] и т.д.
  return variations[attempt - 1] || '';
};

